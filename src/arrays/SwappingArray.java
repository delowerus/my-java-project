package arrays;

public class SwappingArray {
    public static void main(String[] args) {

        //how to swap value

        int a = 6;
        int b = 7;

        int temp;//creating new variable
        temp = a;
        a = b;
        b = temp;
        System.out.println("the value of a is " + a);
        System.out.println("the value of b is " + +b);
    }
}
